#4. 지역변수 VS 전역변수에 대해...
# - 프로그램이 실행되는 내내 변하지 않아야 하거나 여러 함수에서 함께 사용하는 값들은 전역변수로 선언하는 것이 좋으며
#   특정 함수 에서만 사용되는 값들은 지역변수로 선언하는 것이 좋습니다.



























