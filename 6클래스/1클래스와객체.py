#결론 : 이처럼 우리가 실생활에서 접할수 있는 무언가를 속성과 메소드로 분리하여 분석해 보는
#연습을 자주 한다면 이후에 클래스 설계도를 만드는데 훨씬 많은 도움이 될수 있습니다
